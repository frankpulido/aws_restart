print("Python has 3 numeric types: int, float, and complex")
# Numeric types in Python

myValue=1
print((str(myValue)) + " is of data type " + str(type(myValue)))

myOtherValue=1.67
print((str(myOtherValue)) + " is of data type " + str(type(myOtherValue)))

myImaginaryValue=1 + 2j
print((str(myImaginaryValue)) + " is of data type " + str(type(myImaginaryValue)))

print("Python also has boolean values: True and False")
# Boolean values in Python

myBooleanValue=True
print((str(myBooleanValue)) + " is of data type " + str(type(myBooleanValue)))

myOtherBooleanValue=False
print((str(myOtherBooleanValue)) + " is of data type " + str(type(myOtherBooleanValue)))